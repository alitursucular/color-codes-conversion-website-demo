        <p>The <b>CMYK color model (process color, four color)</b> is a subtractive color model, used in color printing,
            and is also used to describe the printing process itself. <b>CMYK</b> refers to the four inks used in some color
            printing:
            <a href="https://en.wikipedia.org/wiki/Cyan" target="_blank"><b>c</b>yan</a>, <a
                href="https://en.wikipedia.org/wiki/Magenta" target="_blank"><b>m</b>agenta</a>, <a
                href="https://en.wikipedia.org/wiki/Yellow" target="_blank"><b>y</b>ellow</a>, and <a
                href="https://en.wikipedia.org/wiki/Key_plate" target="_blank"><b>k</b>ey</a> (<a
                href="https://en.wikipedia.org/wiki/Black" target="_blank">black</a>). Though it varies by print house,
            press operator, press manufacturer, and press run, ink is typically applied in the order of the abbreviation.
        </p>

        <p>The "K" in CMYK stands for key because in four-color printing, cyan, magenta, and yellow printing plates are
            carefully <i>keyed</i>, or aligned, with the <i>key</i> of the black <a
                href="https://en.wikipedia.org/wiki/Key_plate"><i>key plate</i></a>. Some sources suggest that the "K"
            in CMYK comes from the last letter in "black" and was chosen because B already means blue. However, this
            explanation, although useful as a <a href="https://en.wikipedia.org/wiki/Mnemonic" target="_blank">mnemonic</a>,
            is incorrect. K is used as "Key", which was possibly chosen because black is often used as outline.</p>

        <p>The CMYK model works by partially or entirely masking colors on a lighter, usually white, background. The ink
            reduces the light that would otherwise be reflected. Such a model is called <i>subtractive</i> because inks
            "subtract" <a href="https://en.wikipedia.org/wiki/Brightness" target="_blank">brightness</a> from white.</p>
        <br>

        <p><strong>Halftoning</strong>
            <small><i>(Main article: <a href="https://en.wikipedia.org/wiki/Halftone" target="_blank">Halftone)</a></i>
            </small>
        </p>

        <p>With CMYK printing, <a href="https://en.wikipedia.org/wiki/Halftone" target="_blank">halftoning</a> (also
            called screening) allows for less than full saturation of the primary
            colors; tiny dots of each primary color are printed in a pattern small enough that human beings <a
                href="https://en.wikipedia.org/wiki/Color_vision">perceive</a> a
            solid color. Magenta printed with a 20% halftone, for example, produces a pink color, because the eye
            perceives the tiny magenta dots on the large white paper as lighter and less saturated than the color of
            pure magenta ink.</p>

        <p>Without halftoning, the three primary process colors could be printed only as solid blocks of color, and
            therefore could produce only seven colors: the three primaries themselves, plus three secondary colors
            produced by layering two of the primaries: cyan and yellow produce green, cyan and magenta produce blue,
            yellow and magenta produce red (these subtractive secondary colors correspond roughly to the additive
            primary colors) plus layering all three of them resulting in black. With halftoning, a full continuous range
            of colors can be produced.</p><br>

