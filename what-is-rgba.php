        <p>The <b>RGB color model</b> is an additive color model in which red, green, and blue light are added together
            in
            various ways to reproduce a broad array of colors. The name of the model comes from the initials of the
            three additive primary colors, red, green, and blue.
        </p>

        <p>The main purpose of the RGB color model is for the sensing, representation, and display of images in
            electronic systems, such as televisions and computers, though it has also been used in conventional
            photography. Before the electronic age, the RGB color model already had a solid theory behind it, based in
            human perception of colors.</p>

        <p>RGB is a device-dependent color model: different devices detect or reproduce a given RGB value differently,
            since the color elements (such as phosphors or dyes) and their response to the individual R, G, and B levels
            vary from manufacturer to manufacturer, or even in the same device over time. Thus an RGB value does not
            define the same color across devices without some kind of color management.</p><br>

        <p><strong>Additive primary colors</strong></p>

        <p>To form a color with RGB, three light beams (one red, one green, and one blue) must be superimposed (for
            example by emission from a black screen, or by reflection from a white screen). Each of the three beams is
            called a component of that color, and each of them can have an arbitrary intensity, from fully off to fully
            on, in the mixture.</p>

        <p>The RGB color model is additive in the sense that the three light beams are added together, and their light
            spectra add, wavelength for wavelength, to make the final color's spectrum</p>

        <p>Zero intensity for each component gives the darkest color (no light, considered the black), and full
            intensity of each gives a white; the quality of this white depends on the nature of the primary light
            sources, but if they are properly balanced, the result is a neutral white matching the system's white point.
            When the intensities for all the components are the same, the result is a shade of gray, darker or lighter
            depending on the intensity. When the intensities are different, the result is a colorized hue, more or less
            saturated depending on the difference of the strongest and weakest of the intensities of the primary colors
            employed.</p><br>

        <p><strong>Physical principles for the choice of red, green, and blue</strong></p>

        <p>The choice of primary colors is related to the physiology of the human eye; good primaries are stimuli that
            maximize the difference between the responses of the cone cells of the human retina to light of different
            wavelengths, and that thereby make a large color triangle.</p>

        <p>The normal three kinds of light-sensitive photoreceptor cells in the human eye (cone cells) respond most to
            yellow (long wavelength or L), green (medium or M), and violet (short or S) light (peak wavelengths near 570
            nm, 540 nm and 440 nm, respectively). The difference in the signals received from the three kinds allows
            the brain to differentiate a wide gamut of different colors, while being most sensitive (overall) to
            yellowish-green light and to differences between hues in the green-to-orange region.</p>

        <p>As an example, suppose that light in the orange range of wavelengths (approximately 577 nm to 597 nm) enters
            the eye and strikes the retina. Light of these wavelengths would activate both the medium and long
            wavelength cones of the retina, but not equallyâ€”the long-wavelength cells will respond more. The difference
            in the response can be detected by the brain, and this difference is the basis of our perception of orange.
            Thus, the orange appearance of an object results from light from the object entering our eye and stimulating
            the different cones simultaneously but to different degrees.</p>

        <p>Use of the three primary colors is not sufficient to reproduce all colors; only colors within the color
            triangle defined by the chromaticities of the primaries can be reproduced by additive mixing of non-negative
            amounts of those colors of light.</p><br>

